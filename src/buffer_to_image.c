#include "wolf.h"

void	buffer_to_image(t_env *e, int x)
{
	int j;
	j = -1;
	while (++j < HEIGHT)
	{	
		if (x > 120 || j > 120)
			put_pixel_img(e, x, j, e->buffer[j][x]);		// buffer[j][i] = 0;	
	}
}
